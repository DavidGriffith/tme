	    echo "    cond = TME_BIT(TME_FIELD_MASK_EXTRACTU(TME_SPARC_INSN, (0xf << 14)));"
  	    echo ""
	    echo "    /* if the condition is not true: */"
	    echo "    if (!(conds_mask & cond)) {"
	    echo ""
	    echo "      /* return now: */"
	    echo "      /* NB that this may expose us to guests, since we do not check"
